/*============================================================================
    COPARISON OPERATORS

    >      Bigger.
    <      Smaller.
    >=     Bigger than.
    <=     Less than.
    ==     Equal.
    ===    Same and the same type.
    !=     Different than.
    !===   Different, even of the same type.

=============================================================================*/


console.log(5 > 4)
console.log(5 < 4)
console.log(5 >= 4)
console.log(4 <= 4)